// Variables and Data Types

fn main() {
    // Variable declarations
    let mut name = "Sisovin"; // String
    let mut age = 53; // Integer
    let gpa = 1.69; // Float
    let is_tall = true; // Boolean

    // Printing variables
    println!("Your name is {}", name);
    println!("Your name is {}", name);
    println!("Your name is {} and your age is {}", name, age);
    println!("Your GPA is {}", gpa);
    println!("Are you tall? {}", is_tall);

    // Reassigning variables
    name = "Sovanny";
    age = 52;

    // Printing variables after reassignment
    println!("Your name is {}", name);
    println!("Your name is {}", name);
    println!("Your name is {} and your age is {}", name, age);
}
